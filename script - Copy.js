// Hero image slider animation
document.addEventListener('DOMContentLoaded', function() {
    const heroSlider = document.querySelector('.hero-slider');
    if (heroSlider) {
        const images = heroSlider.querySelectorAll('img');
        let currentImageIndex = 0;
        let autoSlideInterval;

        function goToSlide(index) {
            // Clear auto-slide interval when user manually navigates
            clearInterval(autoSlideInterval);
            
            // Hide current image
            images[currentImageIndex].style.transform = 'translateX(-100%)';
            
            // Show new image
            currentImageIndex = index;
            images[currentImageIndex].style.transform = 'translateX(0)';
            
            // Move all other images to the right
            images.forEach((img, i) => {
                if (i !== currentImageIndex) {
                    img.style.transform = 'translateX(100%)';
                }
            });
            
            // Restart auto-slide after manual interaction
            autoSlideInterval = setInterval(nextImage, 4000);
        }

        function nextImage() {
            const nextIndex = (currentImageIndex + 1) % images.length;
            goToSlide(nextIndex);
        }

        function prevImage() {
            const prevIndex = (currentImageIndex - 1 + images.length) % images.length;
            goToSlide(prevIndex);
        }

        // Global functions for onclick handlers
        window.goToNextSlide = nextImage;
        window.goToPrevSlide = prevImage;

        // Start auto-slide
        autoSlideInterval = setInterval(nextImage, 4000);
    }
});

