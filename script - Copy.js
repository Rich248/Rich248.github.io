// Counter animation for stats
function animateCounter(element, target, duration = 2000) {
    const start = 0;
    const increment = target / (duration / 16);
    let current = start;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= target) {
            current = target;
            clearInterval(timer);
        }
        
        // Format the number
        if (element.textContent.includes('K+')) {
            element.textContent = (current / 1000).toFixed(0) + 'K+';
        } else if (element.textContent.includes('10B+')) {
            element.textContent = (current / 1000000000).toFixed(0) + 'B+';
        } else if (element.textContent.includes('+')) {
            element.textContent = Math.floor(current) + '+';
        } else {
            element.textContent = Math.floor(current);
        }
    }, 16);
}

// Initialize counter animations when stats are visible
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting && !entry.target.classList.contains('animated')) {
            entry.target.classList.add('animated');
            const statValue = entry.target.querySelector('h3');
            const text = statValue.textContent;
            
            let target = 0;
            if (text.includes('500K+')) target = 500000;
            else if (text.includes('100+')) target = 100;
            else if (text.includes('10B+')) target = 10000000000;
            
            animateCounter(statValue, target);
        }
    });
}, { threshold: 0.5 });

